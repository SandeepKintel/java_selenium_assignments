package Assignment32_July24;
public class Asg32_SwitchCase {
	public static void main(String[] args) {
		int a = 50;
		int b = 10;
		
		switch(2) 
		{
		case 1: int sum = a+b; 
		{
			System.out.println(sum);
			break;
			
		}
		case 2: int sub = a-b; 
		{
			System.out.println(sub);
			break;
		}
		case 3: int multiply = a*b;
		{
			System.out.println(multiply);
		}
		}
	}
}
